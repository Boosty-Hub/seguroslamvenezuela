#!/usr/bin/env node
// embed-provision.mjs
// Build-time codegen: embeds SQL migrations and Edge Function sources into
// TypeScript modules so the provision routes can use them at runtime without
// reading the filesystem (works on Netlify/Vercel serverless).
//
// Run: node scripts/embed-provision.mjs (from web/ or repo root)
// Auto-run: predev / prebuild hooks in web/package.json

import { readFileSync, readdirSync, writeFileSync, existsSync, mkdirSync } from "fs";
import { join, resolve, basename, dirname } from "path";
import { fileURLToPath } from "url";
import { createHash } from "crypto";

// Hash determinista del contenido de una función (sus archivos), para detectar
// cuándo una Edge Function cambió respecto a la desplegada (centro de updates).
function hashFiles(files) {
  const h = createHash("sha256");
  for (const f of [...files].sort((a, b) => a.path.localeCompare(b.path))) {
    h.update(f.path);
    h.update("\0");
    h.update(f.body);
    h.update("\0");
  }
  return h.digest("hex").slice(0, 16);
}

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Resolve paths relative to web/ regardless of where the script is called from
const webDir = resolve(__dirname, "..");
const supabaseDir = resolve(webDir, "../supabase");
const outDir = resolve(webDir, "src/lib/provision");

// Fail hard if supabase/ directory is missing (required for build integrity)
if (!existsSync(supabaseDir)) {
  console.error(
    `ERROR: Cannot find supabase/ directory at: ${supabaseDir}\n` +
    `The embed-provision script requires the full repository checkout.\n` +
    `Make sure your build deploys from the repo root, not from web/ alone.`
  );
  process.exit(1);
}

const migrationsDir = join(supabaseDir, "migrations");
const functionsDir = join(supabaseDir, "functions");
const sharedDir = join(functionsDir, "_shared");

if (!existsSync(migrationsDir)) {
  console.error(`ERROR: migrations directory not found at: ${migrationsDir}`);
  process.exit(1);
}

if (!existsSync(functionsDir)) {
  console.error(`ERROR: functions directory not found at: ${functionsDir}`);
  process.exit(1);
}

// Ensure output directory exists
if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}

// ─── MIGRATIONS ──────────────────────────────────────────────────────────────

const migrationFiles = readdirSync(migrationsDir)
  .filter((f) => f.endsWith(".sql"))
  .sort(); // lexical order = chronological (0001, 0002, …)

if (migrationFiles.length === 0) {
  console.error(`ERROR: No SQL files found in ${migrationsDir}`);
  process.exit(1);
}

const migrations = migrationFiles.map((filename) => {
  const sql = readFileSync(join(migrationsDir, filename), "utf8");
  return { filename, sql };
});

const migrationsTs = `// AUTO-GENERATED by scripts/embed-provision.mjs — DO NOT EDIT
// Re-generated on every \`pnpm dev\` and \`pnpm build\` via predev/prebuild hooks.
// Source: supabase/migrations/*.sql (${migrations.length} files)
//
// NOTE: \${SUPABASE_URL} placeholders in SQL strings are intentionally preserved
// intact here. Substitution happens at RUNTIME in /api/provision/migrate.

export const MIGRATIONS: { filename: string; sql: string }[] =
${JSON.stringify(migrations, null, 2)} as const;
`;

writeFileSync(join(outDir, "migrations.generated.ts"), migrationsTs, "utf8");
console.log(`[embed-provision] Wrote ${migrations.length} migrations → src/lib/provision/migrations.generated.ts`);

// ─── FUNCTIONS ───────────────────────────────────────────────────────────────

// Read all _shared files (included in every function bundle)
const sharedFiles = existsSync(sharedDir)
  ? readdirSync(sharedDir)
      .filter((f) => !f.startsWith("."))
      .map((filename) => {
        const body = readFileSync(join(sharedDir, filename), "utf8");
        // In the deploy bundle, _shared/ sits as a SIBLING of the <slug>/ dir
        // that holds index.ts. From "<slug>/index.ts", the import
        // "../_shared/config.ts" then resolves to this "_shared/<file>" at the
        // bundle root. (Proven against the Management API: a root-level index.ts
        // would make "../_shared" escape above the root and fail to resolve.)
        return { path: `_shared/${filename}`, body };
      })
  : [];

// Read each function slug (exclude _shared)
const functionSlugs = readdirSync(functionsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory() && d.name !== "_shared")
  .map((d) => d.name)
  .sort();

if (functionSlugs.length === 0) {
  console.error(`ERROR: No function directories found in ${functionsDir}`);
  process.exit(1);
}

const functions = functionSlugs.map((slug) => {
  const indexPath = join(functionsDir, slug, "index.ts");
  if (!existsSync(indexPath)) {
    console.error(`ERROR: Missing index.ts for function '${slug}' at: ${indexPath}`);
    process.exit(1);
  }
  const indexBody = readFileSync(indexPath, "utf8");

  // Bundle layout (multipart virtual FS root):
  //   <slug>/index.ts        ← entrypoint, nested one level deep
  //   _shared/config.ts      ← sibling of <slug>/
  // So "../_shared/config.ts" from the entrypoint resolves to "_shared/...".
  // Emitting index.ts at the root instead would make "../_shared" escape the
  // bundle root and the function would crash on boot.
  const files = [
    { path: `${slug}/index.ts`, body: indexBody },
    ...sharedFiles,
  ];

  return {
    slug,
    entrypoint: `${slug}/index.ts`,
    files,
    hash: hashFiles(files),
  };
});

const functionsTs = `// AUTO-GENERATED by scripts/embed-provision.mjs — DO NOT EDIT
// Re-generated on every \`pnpm dev\` and \`pnpm build\` via predev/prebuild hooks.
// Source: supabase/functions/*/index.ts + _shared/* (${functions.length} functions)
//
// Each function bundle includes its own index.ts PLUS all _shared/* files so
// that relative imports like \`import { loadConfig } from "../_shared/config.ts"\`
// resolve correctly when deployed via Management API multipart.

export const FUNCTIONS: {
  slug: string;
  entrypoint: string;
  files: { path: string; body: string }[];
  hash: string;
}[] = ${JSON.stringify(functions, null, 2)} as const;
`;

writeFileSync(join(outDir, "functions.generated.ts"), functionsTs, "utf8");
console.log(`[embed-provision] Wrote ${functions.length} functions → src/lib/provision/functions.generated.ts`);
console.log(`[embed-provision] Done.`);
